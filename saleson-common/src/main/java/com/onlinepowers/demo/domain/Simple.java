package com.onlinepowers.demo.domain;

public class Simple {
	private String simpleName;
	private String metaKeywords;
	private String metaDescription;
	private String themewordTitle;
	private String themewordDescription;
	public String getSimpleName() {
		return simpleName;
	}
	public void setSimpleName(String simpleName) {
		this.simpleName = simpleName;
	}
	public String getMetaKeywords() {
		return metaKeywords;
	}
	public void setMetaKeywords(String metaKeywords) {
		this.metaKeywords = metaKeywords;
	}
	public String getMetaDescription() {
		return metaDescription;
	}
	public void setMetaDescription(String metaDescription) {
		this.metaDescription = metaDescription;
	}
	public String getThemewordTitle() {
		return themewordTitle;
	}
	public void setThemewordTitle(String themewordTitle) {
		this.themewordTitle = themewordTitle;
	}
	public String getThemewordDescription() {
		return themewordDescription;
	}
	public void setThemewordDescription(String themewordDescription) {
		this.themewordDescription = themewordDescription;
	}
	
	
}
