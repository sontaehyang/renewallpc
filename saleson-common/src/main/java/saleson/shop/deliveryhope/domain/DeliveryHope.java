package saleson.shop.deliveryhope.domain;

public class DeliveryHope {
	
	private int deliveryHopeId;
	private String deliveryHopeTime;
	private int deliveryHopeIndex;
	
	
	
	public int getDeliveryHopeId() {
		return deliveryHopeId;
	}
	public void setDeliveryHopeId(int deliveryHopeId) {
		this.deliveryHopeId = deliveryHopeId;
	}
	public String getDeliveryHopeTime() {
		return deliveryHopeTime;
	}
	public void setDeliveryHopeTime(String deliveryHopeTime) {
		this.deliveryHopeTime = deliveryHopeTime;
	}
	public int getDeliveryHopeIndex() {
		return deliveryHopeIndex;
	}
	public void setDeliveryHopeIndex(int deliveryHopeIndex) {
		this.deliveryHopeIndex = deliveryHopeIndex;
	}
}
